function generateSeatsForBus(bus: Bus): Seat[] {
  const seats: Seat[] = [];
  const isSleeperType = bus.type.includes("Sleeper");
  const cols = isSleeperType ? ["A", "B"] : ["A", "B", "C", "D"];

  // ~20% booked randomly but deterministically
  const bookedPositions = new Set<number>();
  const total = bus.totalSeats;
  const bookedCount = Math.floor(total * 0.2);
  // Use deterministic pseudo-random based on bus id
  let seed = bus.id.charCodeAt(bus.id.length - 1);
  for (let i = 0; i < bookedCount; i++) {
    seed = (seed * 1103515245 + 12345) & 0x7fffffff;
    bookedPositions.add(seed % total);
  }

  let seatIndex = 0;
  const rowCount = Math.ceil(total / cols.length);

  for (let row = 1; row <= rowCount; row++) {
    for (const col of cols) {
      if (seatIndex >= total) break;
      const seatNumber = `${row}${col}`;
      const isWindow = col === "A" || col === (isSleeperType ? "B" : "D");
      const isUpper = isSleeperType && row % 2 === 0;

      seats.push({
        id: `${bus.id}_seat_${seatNumber}`,
        busId: bus.id,
        seatNumber,
        row,
        col,
        status: bookedPositions.has(seatIndex) ? "booked" : "available",
        isWindow,
        isUpper,
      });
      seatIndex++;
    }
  }

  return seats;
}

export function generateAllSeats(): Record<string, Seat[]> {
  const allSeats: Record<string, Seat[]> = {};
  for (const bus of SEED_BUSES) {
    allSeats[bus.id] = generateSeatsForBus(bus);
  }
  return allSeats;
}

